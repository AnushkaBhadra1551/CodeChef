t = int(input())
for i in range(0, t):
    c = input()

    if(c == 'B' or c == 'b'):
        print("BattleShip")

    elif(c == 'C' or c == 'c'):
        print("Cruiser")

    elif(c == 'D' or c == 'd'):
        print("Destroyer")

    elif(c == 'F' or c == 'f'):
        print("Frigate")
    
